    logging.basicConfig(
        level=log_level,
        format='%(asctime)s - %(name)s - %(levelname)s - %(message)s',
        handlers=[
            logging.FileHandler('/opt/monitoring/logs/bot.log'),
            logging.StreamHandler()
        ]
    )
    
    return logging.getLogger(__name__)

